# While Loop
# While loop executes a block of code repeatedly as long as a certain condition is true.The syntax is as follows;
"""
initialization of a variable
while keyword,
followed by the condition/statement to be evaluated,
followed by a full colon(:),
code to be printed out,
increment/decrement
"""
number = 0
while number < 5:
    print("hello Moses", number)
    number = number + 1
print("=================")

number = 50
while number <= 70:
    print(number)
    number = number + 2
print("============")

# below is a decrement example
number = 201
while number <= 150:
    print(number)
    number = number + 3

